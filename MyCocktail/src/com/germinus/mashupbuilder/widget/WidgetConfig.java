package com.germinus.mashupbuilder.widget;

/**
 * This class represents the config of W3C widget.
 */
public class WidgetConfig {
    public static class WidgetAutor {
        /**
         * Widget author.
         */
        private String authorName;
        /**
         * Email of the widget author.
         */
        private String authorEmail;
        /**
         * URL of the widget author.
         */
        private String authorUrl;
        
        /**
         * Gets the widget autor.
         * @return the author
         */
        public String getAuthorName() {
            return authorName;
        }

        /**
         * Sets the widget autor.
         * @param author the widget author to set
         */
        public void setAuthorName(String author) {
            this.authorName = author;
        }

        /**
         * Gets the e-mail of the widget author.
         * @return the e-mail of the widget author
         */
        public String getAuthorEmail() {
            return authorEmail;
        }

        /**
         * Sets the e-mail of the widget author.
         * @param authorEmail the e-mail of the widget author to set
         */
        public void setAuthorEmail(String authorEmail) {
            this.authorEmail = authorEmail;
        }

        /**
         * Gets the URL of the widget author.
         * @return the URL of the widget author
         */
        public String getAuthorUrl() {
            return authorUrl;
        }

        /**
         * Sets the URL of the widget author.
         * @param authorUrl the URL of the widget author to set
         */
        public void setAuthorUrl(String authorUrl) {
            this.authorUrl = authorUrl;
        }
    }
    
    /**
     * Widget version.
     */
    private String version;
    /**
     * Widget name.
     */
    private String name;
    /**
     * Widget description.
     */
    private String description;
    /**
     * Widget license.
     */
    private String license;
    /**
     * Widget author.
     */
    private WidgetAutor author = new WidgetAutor();
    /**
     * Widget height.
     */
    private int height;
    /**
     * Widget width.
     */
    private int width;
    //preferences

    /**
     * Gets the widget version.
     * @return the widget version.
     */
    public String getVersion() {
        return version;
    }

    /**
     * Sets the widget version.
     * @param version the widget version to set.
     */
    public void setVersion(String version) {
        this.version = version;
    }

    /**
     * Gets the name of the widget.
     * @return the name of the widget.
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name of the widget.
     * @param name the name of the widget to set.
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets the widget description.
     * @return the widget description.
     */
    public String getDescription() {
        return description;
    }

    /**
     * Sets the widget description.
     * @param description the widget description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * Gets the widget license.
     * @return the license
     */
    public String getLicense() {
        return license;
    }

    /**
     * Sets the widget license.
     * @param license the widget license to set
     */
    public void setLicense(String license) {
        this.license = license;
    }

    /**
     * Gets the widget autor.
     * @return the author
     */
    public String getAuthorName() {
        return author.getAuthorName();
    }

    /**
     * Sets the widget autor.
     * @param author the widget author to set
     */
    public void setAuthorName(String author) {
        this.author.setAuthorName(author);
    }

    /**
     * Gets the e-mail of the widget author.
     * @return the e-mail of the widget author
     */
    public String getAuthorEmail() {
        return author.getAuthorEmail();
    }

    /**
     * Sets the e-mail of the widget author.
     * @param authorEmail the e-mail of the widget author to set
     */
    public void setAuthorEmail(String authorEmail) {
        this.author.setAuthorEmail(authorEmail);
    }

    /**
     * Gets the URL of the widget author.
     * @return the URL of the widget author
     */
    public String getAuthorUrl() {
        return author.getAuthorUrl();
    }

    /**
     * Sets the URL of the widget author.
     * @param authorUrl the URL of the widget author to set
     */
    public void setAuthorUrl(String authorUrl) {
        this.author.setAuthorUrl(authorUrl);
    }

    /**
     * Gets the widget height.
     * @return the height
     */
    public int getHeight() {
        return height;
    }

    /**
     * Sets the widget height.
     * @param height the height to set
     */
    public void setHeight(int height) {
        this.height = height;
    }

    /**
     * Gets the widget width.
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * Sets the Widget width.
     * @param width the width to set
     */
    public void setWidth(int width) {
        this.width = width;
    }
}
