package com.germinus.mashupbuilder.services;

import java.util.List;
import java.util.ArrayList;

/**
 * Represents an UnitAction in MyCocktail application.
 */
public class UnitAction {
    /**
     * The type of the UnitActiont.
     */
    private String type;
    /**
     * The label of the UnitAction.
     */
    private String label;
    /**
     * The description of the UnitAction.
     */
    private String description;
    /**
     * The list of parameters accepted by the service.
     */
    private List<Parameter> inputs = new ArrayList<Parameter>();
    /**
     * The execute method of the UnitAction.
     */
    private ExecuteMethod executeMethod;

    /**
     * Returns the type of the UnitAction.
     * @return the type of the UnitAction
     */
    public final String getType() {
        return type;
    }

    /**
     * Sets the type of the UnitAction.
     * @param type the type to set
     */
    public final void setType(final String type) {
        this.type = type;
    }

    /**
     * Returns the label of the UnitAction.
     * @return the label of the UnitAction
     */
    public final String getLabel() {
        return label;
    }

    /**
     * Sets the label of the UnitAction.
     * @param label the label of the UnitAction to set
     */
    public final void setLabel(final String label) {
        this.label = label;
    }

    /**
     * Returns the description of the UnitAction.
     * @return the description of the UnitAction
     */
    public final String getDescription() {
        return description;
    }

    /**
     * Sets the description of the UnitAction.
     * @param description the description of the UnitAction to set
     */
    public final void setDescription(final String description) {
        this.description = description;
    }

    /**
     * Sets the list of parameters accepted by the service.
     * @return the list of parameters accepted by the service.
     */
    public final List<Parameter> getInputs() {
        return inputs;
    }

    /**
     * Sets the list of parameters accepted by the service.
     * @param inputs the list of parameters accepted by the service.
     */
    public final void setInputs(final List<Parameter> inputs) {
        this.inputs = inputs;
    }

    /**
     * Returns the execute method of the UnitAction.
     * @return the execute method of the UnitAction
     */
    public final ExecuteMethod getExecuteMethod() {
        return executeMethod;
    }

    /**
     * Sets the execute method of the UnitAction.
     * @param executeMethod the execute method of the UnitAction to set
     */
    public final void setExecuteMethod(final ExecuteMethod executeMethod) {
        this.executeMethod = executeMethod;
    }
}
